import abc


class FileAccess(abc.ABC):
    def __init__(self):
        # Here path represent the directory, not the file
        self.__path = ""
    
    def getPath(self) -> str:
        return self.__path

    def setPath(self, new_path: str):
        self.__path = new_path
        if len(self.__path) != 0 and self.__path[-1] != '/':
            self.__path += '/'
